//посмотреть методы у Math можно с помощью консоли, просто ввести Math. и появится список
var num = 2.4
console.log(Math.random()) //random - рандомное значение
console.log(Math.floor(num)) //floor - округлит до целлого в меньшую сторону
console.log(Math.ceil(num)) //floor - округлит до целлого в большею сторону

console.log(Math.PI)
console.log(Math.E) //экспонента